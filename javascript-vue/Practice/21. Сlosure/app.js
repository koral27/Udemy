// Создайте функцию которая бы умела делать:
// minus(10)(6); // 4
// minus(5)(6); // -1
// minus(10)(); // 10
// minus()(6); // -6
// minus()(); // 0
// Подсказка, функция minus должна возвращать другую функцию.

let minus = function (a){
    return function(b){
        return a - b
    }
}

let result = minus(10)(6);
console.log(result);

